package cast;

public class CastEntry {
	
	private String path;
	private String totalErrors;
	private String fixEffort;
	
	public CastEntry(){
		super();
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getTotalErrors() {
		return totalErrors;
	}

	public void setTotalErrors(String totalErrors) {
		this.totalErrors = totalErrors;
	}

	public String getFixEffort() {
		return fixEffort;
	}

	public void setFixEffort(String fixEffort) {
		this.fixEffort = fixEffort;
	}
	
	
	
	

}
